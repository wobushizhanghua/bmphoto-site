<?php

defined('ABSPATH') || die('Access Denied');


class EcommercewdViewUninstall extends EcommercewdView {
    ////////////////////////////////////////////////////////////////////////////////////////
    // Events                                                                             //
    ////////////////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////////////
    // Constants                                                                          //
    ////////////////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////////////
    // Variables                                                                          //
    ////////////////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////////////
    // Constructor & Destructor                                                           //
    ////////////////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////////////
    // Public Methods                                                                     //
    ////////////////////////////////////////////////////////////////////////////////////////
    public function display($tpl = null) {
      ?>
    <div class="wrap">
      <?php
        $this->create_toolbar();

        $model = $this->getModel();
        
        $task = WDFInput::get_task();

        switch ($task) {
          case 'remove_sample_data':
            $this->_layout = 'uninstall';
            $model->remove_sample_data();
            WDFHelper::redirect();
            break;
          case 'uninstall':
            $this->_layout = 'uninstall';
            $model->delete_db_tables();
            break;
          default:
            $this->_layout = 'default';
            break;
        }
        
        parent::display($tpl);
        ?>
    </div>
    <?php
    }


    ////////////////////////////////////////////////////////////////////////////////////////
    // Getters & Setters                                                                  //
    ////////////////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////////////
    // Private Methods                                                                    //
    ////////////////////////////////////////////////////////////////////////////////////////
    private function create_toolbar() {
        WDFToolbar::title(__('Uninstall', 'wde'), 'spidershop_uninstall.png');
    }


    ////////////////////////////////////////////////////////////////////////////////////////
    // Listeners                                                                          //
    ////////////////////////////////////////////////////////////////////////////////////////
}