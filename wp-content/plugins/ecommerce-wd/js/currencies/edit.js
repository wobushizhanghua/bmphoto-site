function exploreCurrencies(payment_name, obj) {
  var url = _wde_admin_url + "admin-ajax.php?action=wde_ajax" +
      "&page=wde_currencies&task=explore_currencies&callback=insertCurrencyData&payment=" + payment_name + "&TB_iframe=1";
  jQuery(obj).attr("href", url);
}

function insertCurrencyData(currencyData) {
  adminFormSet("name", currencyData["name"]);
  adminFormSet("code", currencyData["code"]);
  adminFormSet("sign", currencyData["sign"]);
}

