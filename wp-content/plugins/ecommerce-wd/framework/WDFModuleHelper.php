	public static function get_module_name( $id )
	{
		global $wpdb;
		$query = $wpdb->getQuery(true);
		
		$query->clear();
		$query->select('module');
		$query->from('#__modules');
	
		$query->where( "id = " . $id );

		$wpdb->setQuery($query);
		$module_name = $wpdb->loadResult();

		if ($wpdb->getErrorNum()) {
			echo $wpdb->getErrorMsg();
			return false;
		}
		
		return $module_name ? $module_name : false;

	}
	
	public static function get_module_params( $name )
	{		
		global $wpdb;
		$query = $wpdb->getQuery(true);
		
		$query->clear();
		$query->select('params');
		$query->from('#__modules');
	
		$query->where( "module = '" . $name . "'");

		$wpdb->setQuery($query);
		$module_params = $wpdb->loadResult();
	
		if ($wpdb->getErrorNum()) {
			echo $wpdb->getErrorMsg();
			return false;
		}
		return WDFJson::decode($module_params);
	}



